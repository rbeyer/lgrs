class _AbstractBaseCoordinate(_abc.ABC):
    pass

# Note: `_BaseCoordinate` is useful for defining behavior that depends
# on the class being a dataclass. Conversely, `BaseCoordinate` and its
# hidden subclasses are useful for defining all other behavior (without
# accidentally implying dataclass fields).
@_dataclasses.dataclass(frozen=True, kw_only=True)
class _BaseCoordinate(_AbstractBaseCoordinate):

    #* Fields and validation. -------------------------------------------------
    _fields_cached: _typing.ClassVar[tuple[_dataclasses.Field, ...]]
    prefer_lps: bool = False
    extended_ltm: bool = False
    polar_ltm: bool = False
    validate: _dataclasses.InitVar[bool] = True

    def _register_validation(self) -> None:
        object.__setattr__(self, "_was_validated", True)

    def _validate(self) -> None:
        for field in self._get_fields():
            result = getattr(self, f"_validate_{field.name}")()
            if result is not None:
                # Note: Cannot assign directly, because `self` is a
                # frozen dataclass.
                object.__setattr__(self, field.name, result)
        # Note: `._init_kwargs` effectively freezes values, which may
        # not be finalized until validation completes. It should not be
        # assigned until after validation.
        assert "_init_kwargs" not in self.__dict__
        self._register_validation()

    def _validate_polar_ltm(self) -> None:
        if self.polar_ltm:
            if (self.prefer_lps or self.extended_ltm):
                raise _exceptions.MalformedCoordinate(
                    "If `polar_ltm` is `True`, `prefer_lps` and `extended_ltm` "
                    "must be `False` (or `None`)."
                )

    _validate_prefer_lps = _return_none
    _validate_extended_ltm = _return_none

    #* Initialization. --------------------------------------------------------
    def __post_init__(self, validate: bool) -> None:
        if validate:
            self._validate()

    @_functools.cached_property
    def _init_kwargs(self) -> dict[str, _typing.Any]:
        return {
            field.name: getattr(self, field.name)
            for field in self._get_fields()
        }

    def with_constraints(
            self, *,
            prefer_lps: bool | None = None, extended_ltm: bool | None = None,
            polar_ltm: bool | None = None, validate: bool | None = None,
            copy: bool = False
    ) -> _typing.Self:
        # Resolve new initialization kwargs.
        new_init_kwargs = self._init_kwargs.copy()
        if prefer_lps is not None:
            new_init_kwargs["prefer_lps"] = prefer_lps
        if polar_ltm is not None:
            new_init_kwargs["polar_ltm"] = polar_ltm
        if extended_ltm is not None:
            new_init_kwargs["extended_ltm"] = extended_ltm

        # Return `self`, if suitable and allowed.
        if (
            not copy
            and not validate  # May be `None`.
            and new_init_kwargs == self._init_kwargs
        ):
            return self

        # Create and return copy, constrained as specified.
        if validate is None:
            validate = True  # *REASSIGNMENT*
        new = type(self)(**new_init_kwargs, validate=validate)
        return new

    #* Field support. ---------------------------------------------------------
    def __iter__(self) -> _collections.abc.Iterable:
        return iter(self._init_kwargs.values())

    @_functools.cached_property
    def _constraint_keys(self) -> tuple[str, ...]:
        # Note: Assumes that all fields of `_BaseCoordinate` are
        # constraints, consistent with `_easy_dataclass()`.
        constraint_keys = tuple(
            field.name
            for field in _BaseCoordinate._get_fields()
        )
        _BaseCoordinate._constraint_keys = constraint_keys
        return constraint_keys

    @classmethod
    @_functools.cache
    def _get_fields(cls) -> tuple[_dataclasses.Field, ...]:
        return _dataclasses.fields(cls)

    @classmethod
    @_functools.cache
    def _get_field_name_to_type(cls) -> dict[str, type]:
        name_to_type = {}
        field_name_set = {
            field.name
            for field in cls._get_fields()
        }
        for name, typ in _typing.get_type_hints(cls).items():
            if name not in field_name_set:
                continue
            if isinstance(typ, _types.UnionType):
                types = list(_typing.get_args(typ))
                types.remove(type(None))
                typ, = types  # *REASSIGNMENT*
            name_to_type[name] = typ
        return name_to_type

    @_functools.cached_property
    def _nonconstraint_kwargs(self) -> dict[str, _typing.Any]:
        return {
            field_name: field_val
            for field_name, field_val in self._init_kwargs.items()
            if field_name not in self._constraint_keys
        }


class BaseCoordinate(_BaseCoordinate):
    # TODO: Document that `MalformedCoordinate` may be raised.
    _template: _collections.abc.Callable | str | None = None
    _was_validated: bool = False

    #* Basic behavior. --------------------------------------------------------
    def __copy__(self) -> _typing.Self:
        return self.copy()

    def __str__(self) -> str:
        return self.string

    #* Instantiation. ---------------------------------------------------------
    @classmethod
    def _from_ref_string(cls, string: str) -> _typing.Self:
        # Note: Unlike `_GriddedCoordinate.from_string()`, this method
        # exists solely to support parsing values from
        # `LGRS_Coordinate_Conversion`.
        parts = tuple(string.split(" "))
        if len(parts) == 1:
            raise _exceptions.MalformedCoordinate(
                "`string` must be space-delimited"
            )
        field_name_to_type = cls._get_field_name_to_type()
        if len(parts) > len(field_name_to_type):
            raise _exceptions.MalformedCoordinate(
                "`string` contains too many space-delimited "
                f"components: {string!r}"
            )
        init_kwargs = {
            name: typ(part)
            for part, (name, typ) in zip(parts, field_name_to_type.items())
        }
        return cls(**init_kwargs)

    #* Validation. ------------------------------------------------------------
    def _raise_malformed_coordinate(
            self, middle: str, *,
            attr_name: str, if_attr_name: str | None = None,
    ) -> _typing.NoReturn:
        if if_attr_name is None:
            prefix = ""
        else:
            prefix = f"For `{if_attr_name}={getattr(self, if_attr_name)!r}`, "
        raise _exceptions.MalformedCoordinate(
            f"{prefix}"
            f"`{attr_name}` must be "
            f"{middle}"
            f", not: {getattr(self, attr_name)!r}"
        )

    @staticmethod
    def _raise_unexpected(
            func: _collections.abc.Callable | None = None
    ) -> _typing.NoReturn:
        if func is not None:
            func()
        raise TypeError("An unexpected error occurred.")

    def _validate_against_closed_interval(
        self, *, attr_name: str, minimum: _typing.Any, maximum: _typing.Any,
        if_attr_name: str | None = None, coerce_str: bool = False
    ) -> None:
        val = getattr(self, attr_name)
        if coerce_str:
            val = float(val)  # *REASSIGNMENT*
        if not (minimum <= val <= maximum):
            self._raise_malformed_coordinate(
                f"between {minimum} and {maximum}, inclusive",
                attr_name=attr_name, if_attr_name=if_attr_name
            )

    def _validate_against_sequence(
        self, *,
        attr_name: str, sequence: _collections.abc.Sequence,
        if_attr_name: str | None = None
    ) -> None:
        val = getattr(self, attr_name)
        if val not in sequence:
            if len(sequence) == 2:
                item_1, item_2 = sequence
                middle = f"{item_1!r} or {item_2!r}"
            else:
                middle = (
                    f"one of {', '.join(map(repr, sequence[:-1]))}, "
                    f"or {sequence[-1]!r}"
                )
            self._raise_malformed_coordinate(
                middle=middle, attr_name=attr_name, if_attr_name=if_attr_name
            )

    def _validate_hemisphere(self) -> None:
        self._validate_against_sequence(
            attr_name="hemisphere", sequence=("N", "S")
        )

    def validate(self, *, revalidate: bool = False) -> None:
        if not revalidate and self._was_validated:
            return
        new = self.copy(validate=False)
        try:
            new._validate()
        except _exceptions.MalformedCoordinate:
            self._unregister_cousins(*self._cousins)
            raise
        if new._init_kwargs == self._init_kwargs:
            self._register_validation()
            return
        self._unregister_cousins(*self._cousins)
        change_lines = []
        for k, new_v in new._init_kwargs.items():
            old_v = self._init_kwargs[k]
            if new_v != old_v:
                change_lines.append(
                    f"    {k}: {old_v!r} --> {new_v!r}"
                )
        raise _exceptions.MalformedCoordinate(
            "\n"
            "  Validation conformed the following value(s):\n"
            f"{'\n'.join(change_lines)}"
        )

    #* Public data. -----------------------------------------------------------
    @_functools.cached_property
    def string(self) -> str:
        match self._template:
            case None:
                string = "".join(self._iter_value_strings())
            case str():
                string = self._template.format(**self._init_kwargs)
            case _:
                string = self._template()
        return string

    #* General public methods. ------------------------------------------------
    @classmethod
    def _is_x_based(cls, prefix: str) -> bool:
        if issubclass(cls, LatLonPoint):
            raise TypeError("`LatLonPoint` is neither LPS- nor LTM-based")
        return cls.__name__.startswith(prefix)
    #*
    @staticmethod
    def _conform_to_latlon_points(
            *coords: BaseCoordinate, center: bool
    ) -> _typing.Iterator[LatLonPoint]:
        for coord in coords:
            if center and isinstance(coord, _GriddedCoordinate):
                yield coord.center_latlon
            else:
                yield coord.to_latlon()

    @property
    def _geod(self) -> _pyproj.Geod:
        geod = _pyproj.Geod(sphere=True, a=_wkt.LUNAR_RADIUS)
        type(self)._geod = geod  # Reuse this instance.
        return geod

    def copy(self, *, validate: bool = False) -> _typing.Self:
        # Note: `self` can only exist if validated or explicitly not
        # validated. Either way, defaulting `validate` to `False` is
        # appropriate.
        new = type(self)(
            **self._init_kwargs, validate=(validate and not self._was_validated)
        )
        return new

    def distance_to(
            self, other: BaseCoordinate, *, center: bool = False
    ) -> float:
        self_latlon_point, other_latlon_point = self._conform_to_latlon_points(
            self, other, center=center
        )
        _, _, dist = self._geod.inv(
            self_latlon_point.longitude, self_latlon_point.latitude,
            other_latlon_point.longitude, other_latlon_point.latitude
        )
        return dist

    def grid_distance_to(
            self, other: BaseCoordinate, *, center: bool = False,
            system: str | None = None
    ) -> float:
        match system.upper():
            case "LPS":
                convert = BaseCoordinate.to_lps
            case "LTM":
                convert = BaseCoordinate.to_ltm
            case None:
                convert = BaseCoordinate.to_lps_or_ltm
            case _:
                raise TypeError(f"`system` not recognized: {system!r}")
        self_box, other_box = map(
            convert,
            self._conform_to_latlon_points(self, other, center=center)
        )
        if (
                system is None
                and self_box.is_lps_based() != other_box.is_lps_based()
        ):
            raise TypeError(
                "`self` and `other` are not in the same system (LPS or LTM)"
            )
        dist = _math.dist(
            (self_box.easting, self_box.northing),
            (other_box.easting, other_box.northing)
        )
        return dist

    def is_equal_to(
            self, other: _typing.Self, *, error: bool = False, constraints: bool = False
    ) -> bool:
        # Validate type of `other`.
        # TODO: Once library-wide type guards are implemented, remove
        #  this block.
        if not isinstance(other, type(self)):
            raise TypeError(
                f"`other` must be of type {type(self).__name__}, not: {other!r}"
            )

        # Compare.
        if constraints:
            included_field_names = self._constraint_keys
        else:
            included_field_names = self._init_kwargs.keys()
        err_lines = []
        for field_name, self_val in self._init_kwargs.items():
            if field_name not in included_field_names:
                continue
            other_val = getattr(other, field_name)
            if self_val != other_val:
                if not error:
                    return False
                err_lines.append(f"  {field_name!r} values differ:")
                err_lines.append(f"    {self_val!r} vs. {other_val!r}")
        if err_lines:
            raise TypeError("\n" + "\n".join(err_lines))
        return True

    # TODO: Consider an `.is_close_to()` that automatically accounts for
    #  both `.precision` (where applicable) and the inferred conversion
    #  sequence. For example, comparing a `LatLonPoint` and a `LpsAcc`
    #  would allow for expected imprecision in the `LatLonPoint` -->
    #  `LpsPoint` --> `LpsLgrs` sequence, and comparing a `LatLonPoint`
    #  to a `LatLonPoint` would allow for the expected round-trip
    #  disparity.

    @classmethod
    @_functools.cache
    def is_lps_based(cls):
        return cls._is_x_based("Lps")

    @classmethod
    @_functools.cache
    def is_ltm_based(cls):
        return cls._is_x_based("Ltm")

    def replace(
            self, *, validate: bool = True, copy: bool = True, **overrides
    ) -> _typing.Self:
        if not copy and not overrides:
            return self
        init_kwargs = self._init_kwargs.copy()
        init_kwargs.update(overrides)
        return type(self)(**init_kwargs, validate=validate)


    #* Transformation caching. ------------------------------------------------
    # Note: See `_redirect()` for a description of what a "cousin" is.

    @property
    def _cousins(self) -> _collections.deque[BaseCoordinate]:
        cached_cousins = _caching._query_weak_cache(
            self._root, key="cousins", default=None
        )
        if cached_cousins is None:
            new_cousins = _collections.deque()
            new_cousins.appendleft(self)
            _caching._store_to_weak_cache(
                self._root, key="cousins", value=new_cousins
            )
            return new_cousins
        else:
            return cached_cousins

    def _get_cached_cousin(self, func: ToMethod) -> BaseCoordinate | None:
        # Resolve out types.
        out_types = _resolve_out_types(func)

        # If suitable cousin is found, return it.
        for cousin in self._cousins:
            if isinstance(cousin, out_types):
                return cousin
        return None

    def _register_cousin(self, cousin: BaseCoordinate) -> None:
        if _caching._CACHING_IS_ENABLED:
            object.__setattr__(cousin, "_root", self._root)
            self._cousins.appendleft(cousin)

    @_functools.cached_property
    def _root(self) -> BaseCoordinate:
        # Note: Overridden when instantiated by transformation.
        return self

    def _unregister_cousins(self, *cousins: BaseCoordinate) -> None:
        if _caching._CACHING_IS_ENABLED:
            for cousin in cousins:
                self._cousins.remove(cousin)

    @_functools.cached_property
    def constraints(self) -> _types.MappingProxyType[str, bool]:
        return _types.MappingProxyType(
            {
                key: self._init_kwargs[key]
                for key in self._constraint_keys
            }
        )

    #* Coordinate transformation. ---------------------------------------------
    def _force_type_or_error(
            self, bound_method: _collections.abc.Callable,
            targ_typ: type[BaseCoordinate], **constraints: bool
    ) -> BaseCoordinate:
        cand = bound_method()
        if isinstance(cand, targ_typ):
            return cand
        reconstrained = self.with_constraints(**constraints)
        cand = bound_method.__func__(reconstrained)
        if isinstance(cand, targ_typ):
            return cand
        else:
            raise _exceptions.MalformedCoordinate(
                f"Location is not compatible with `{targ_typ.__name__}`: "
                f"{reconstrained}"
            )

    @staticmethod
    @_functools.cache
    def _get_conversion_sequence(
            targ_type: type[BaseCoordinate]
    ) -> tuple[ToMethod | None, ToMethod]:
        if targ_type is LatLonPoint:
            return (None, BaseCoordinate.to_latlon)
        if targ_type in (LpsPoint, LtmPoint):
            convert = BaseCoordinate.to_lps_or_ltm
        elif targ_type in (LpsLgrsBox, LtmLgrsBox):
            convert = BaseCoordinate.to_lgrs
        elif targ_type in (LpsAccBox, LtmAccBox):
            convert = BaseCoordinate.to_acc
        else:
            BaseCoordinate._raise_unexpected()
        if targ_type.is_lps_based():
            force_system = BaseCoordinate.to_lps
        elif targ_type.is_ltm_based():
            force_system = BaseCoordinate.to_ltm
        else:
            BaseCoordinate._raise_unexpected()
        return (force_system, convert)

    def to(
            self, typ: type[BaseCoordinate], *, any_system: bool = False
    ) -> BaseCoordinate:
        force_system, convert = self._get_conversion_sequence(typ)
        if (
                not any_system
                and force_system is not None
                and self.is_lps_based() != typ.is_lps_based()
            ):
            basis = force_system(self)
        else:
            basis = self
        return convert(basis)

    @_redirect
    def to_acc(self) -> LpsAccBox | LtmAccBox:
        ...

    @_redirect
    def to_latlon(self) -> LatLonPoint:
        ...

    @_redirect
    def to_lgrs(self) -> LpsLgrsBox | LtmLgrsBox:
        ...

    def to_lps(self) -> LpsPoint:
        lps_point = self._force_type_or_error(
            self.to_lps_or_ltm, LpsPoint,
            prefer_lps=True, extended_ltm=False, polar_ltm=False
        )
        return lps_point

    @_redirect
    def to_lps_or_ltm(self) -> LpsPoint | LtmPoint:
        ...

    def to_ltm(self) -> LtmPoint:
        ltm_point = self._force_type_or_error(
            self.to_lps_or_ltm, LtmPoint, polar_ltm=True
        )
        return ltm_point

    # Note: The following four methods permit `._to_*()` methods to be
    # defined in subclasses for just one step in each direction.
    def _to_acc(self) -> LpsAccBox | LtmAccBox:
        if isinstance(self, (LpsPoint, LtmPoint)):
            lps_or_ltm = self
        else:
            assert isinstance(self, LatLonPoint)
            lps_or_ltm = self._to_lps_or_ltm()
        lgrs = lps_or_ltm._to_lgrs()
        acc = lgrs._to_acc()
        return acc

    def _to_latlon(self) -> LatLonPoint:
        if isinstance(self, (LpsLgrsBox, LtmLgrsBox)):
            lgrs = self
        else:
            assert isinstance(self, (LpsAccBox, LtmAccBox))
            lgrs = self._to_lgrs()
        lps_or_ltm = lgrs._to_lps_or_ltm()
        latlon = lps_or_ltm._to_latlon()
        return latlon

    def _to_lgrs(self) -> LpsLgrsBox | LtmLgrsBox:
        assert isinstance(self, LatLonPoint)
        lps_or_ltm = self._to_lps_or_ltm()
        lgrs = lps_or_ltm._to_lgrs()
        return lgrs

    def _to_lps_or_ltm(self) -> LpsPoint | LtmPoint:
        assert isinstance(self, (LpsAccBox, LtmAccBox))
        lgrs = self._to_lgrs()
        lps_or_ltm = lgrs._to_lps_or_ltm()
        return lps_or_ltm

    #* Utilities. -------------------------------------------------------------
    def _iter_value_strings(self) -> _typing.Iterator[str]:
        for value in self:
            match value:
                case str():
                    yield value
                case bool() | None:
                    continue
                case int() | float():
                    yield repr(value)
                case _:
                    self._raise_unexpected()



