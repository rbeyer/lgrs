class _GriddedCoordinate(BaseCoordinate):
    # TODO: Add `.truncate_to()`.

    #* Fields and validation. -------------------------------------------------
    easting:  str | None
    northing: str | None

    def _validate(self) -> None:
        super()._validate()
        self._validate_against_pattern()

    def _validate_against_pattern(self) -> None:
        if self._pattern.search(self.string) is None:
            raise _exceptions.MalformedCoordinate(
                f"`.string` does not have the form: {self._pattern.pattern!r}"
            )

    #* Instantiation from string. ---------------------------------------------
    _pattern: _regex.Pattern

    @classmethod
    def from_string(cls, string: str, *, validate: bool = True) -> _typing.Self:
        # Match to pattern.
        match = cls._pattern.search(string)
        if match is None:
            raise _exceptions.MalformedCoordinate(
                f"`string` {string!r} is not in the supported format: "
                f"{cls._pattern.pattern!r}"
            )
        match_dict = match.groupdict()

        # Coerce each argument to the correct type.
        field_name_to_type = cls._get_field_name_to_type()
        init_kwargs = {
            name: field_name_to_type[name](value_string)
            for name, value_string in match_dict.items()
            if value_string is not None
        }
        return cls(**init_kwargs, validate=validate)

    #* Reference points. ------------------------------------------------------
    def _make_reference_point(
            self, easting_delta: float, northing_delta: float
    ) -> LatLonPoint:
        lps_or_ltm_point = self.to_lps_or_ltm()
        temp = lps_or_ltm_point.replace(
            easting=lps_or_ltm_point.easting + easting_delta,
            northing=lps_or_ltm_point.northing + northing_delta,
            validate=False
        )
        # Note: New instance must be `LatLonPoint`, to be guaranteed
        # valid, and must be a copy, to divorce its cache history from
        # the possibly invalid `temp`.
        new = temp.to_latlon().copy()
        return new

    @_functools.cached_property
    def center_latlon(self) -> LatLonPoint:
        half_precision = (0.5 * self.precision)
        center = self._make_reference_point(+half_precision, +half_precision)
        return center

    #* Public data and methods. -----------------------------------------------
    def contains(
            self, other: BaseCoordinate, *,
            logical: bool = False,  cross_system: bool = True
    ) -> bool:
        # Honor restrictive arguments.
        if logical and not isinstance(other, _GriddedCoordinate):
            return False
        if (
                (logical or not cross_system)
                and  self.is_lps_based() != other.is_lps_based()
        ):
                return False

        # Coerce `other` to same type as `self`.
        try:
            other_box = other.to(type(self))
        except _exceptions.MalformedCoordinate:
            return False

        # If `other` is larger than `self`, `self` cannot contain
        # `other`.
        if other_box.precision > self.precision:
            return False

        # Test whether `other`, expanded to the same size as `self`, is
        # equivalent to `self`.
        return self.is_equal_to(other_box.truncate(self.precision))

    @_functools.cached_property
    def precision(self) -> int:
        lgrs_easting = self.to_lgrs().easting
        # Table 11
        if lgrs_easting is None:
            return 25_000
        match len(lgrs_easting):
            case 5:
                return 1
            case 4:
                return 10
            case 3:
                return 100
            case 2:
                return 1000
            case _:
                self._raise_unexpected()

    def truncate(
            self, min_precision: float, *, copy: bool = False
    ) -> _typing.Self:
        # Determine `*LgrsBox` easting and northing character count.
        if min_precision < 1:
            raise TypeError(
                f"`min_precision` must be >= 1, not: {min_precision!r}"
            )
        elif 1 <= min_precision < 10:
            lgrs_char_count = 5
        elif 10 <= min_precision < 100:
            lgrs_char_count = 4
        elif 100 <= min_precision < 1000:
            lgrs_char_count = 3
        elif 1000 <= min_precision < 25_000:
            lgrs_char_count = 2
        else:
            lgrs_char_count = 0

        # Return `self`, if allowed and suitable.
        self_lgrs_box = self.to_lgrs()
        if (
                not copy
                and len(_as_str(self_lgrs_box.easting)) == lgrs_char_count
        ):
            return self

        # Create and return truncated instance.
        init_kwargs = self_lgrs_box._init_kwargs.copy()
        if lgrs_char_count:
            easting = _as_str(self_lgrs_box.easting)
            northing = _as_str(self_lgrs_box.northing)
            init_kwargs["easting"] = f"{easting}00000"[:lgrs_char_count]
            init_kwargs["northing"] = f"{northing}00000"[:lgrs_char_count]
        else:
            init_kwargs["easting"] = None
            init_kwargs["northing"] = None
        new_lgrs_box = type(self_lgrs_box)(**init_kwargs, validate=False)
        final = new_lgrs_box.to(type(self))
        return final



