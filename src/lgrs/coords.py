def _cache_new_cousin(func: ToMethod) -> ToMethod:
    """
    Cache the new `BaseCoordinate` returned whenever `func()` is called.`.

    See `_redirect()` for a description of what a "cousin" is.

    Parameters
    ----------
    func : callable
        The hidden `._to_*()` method to be decorated.

    Returns
    -------
    wrapped : callable
        The wrapped `func`.
    """
    @_functools.wraps(func)
    def wrapped(self: BaseCoordinate, **kwargs) -> BaseCoordinate:
        new = func(self, **kwargs)
        self._register_cousin(new)
        return new
    return wrapped

def _calc_na_letterset(zone_number: int) -> int:
    # TODO: Determine if the "- 1" (which appears in the reference
    #  code but not in Eq. 83) is correct.
    na_letterset = (zone_number - 1) % 3  # Eq. 83
    return na_letterset

def _redirect(func: ToMethod) -> ToMethod:
    """
    For `.to_*()`, get from cache or redirect to `._to_*()`.

    A method decorated with this function will never be called (and
    therefore can be empty). Rather, a suitable "cousin" `BaseCoordinate`
    instance is returned from the cache, if possible. Otherwise, the call
    is redirected to `self._to_*()` for creation and the new instance is
    returned.

    A "cousin" is another `BaseCoordinate` whose location (after
    transformation) and constraints are the same as those of `self`. Cousins
    are often generated during intermediate calculations, whether internal
    or external (i.e., by the user), so caching them into cousin groups
    improves efficiency. Note that a cousin group is only guaranteed to
    include those cousins generated by a transformation chain from a
    common root. Therefore, cousins that are wholly independently
    instantiated may not be grouped together.

    Parameters
    ----------
    func : callable
        The public `.to_*()` method to be decorated. It will never be
        called.

    Returns
    -------
    wrapped : callable
        The wrapped `func`.
    """
    @_functools.wraps(func)
    def wrapped(self: BaseCoordinate) -> BaseCoordinate:
        # Note: `func` itself is only used for its return type and name.
        cached = self._get_cached_cousin(func)
        if cached is not None:
            return cached
        new = getattr(self, f"_{func.__name__}")()
        return new
    return wrapped

@_functools.cache
def _resolve_out_types(func: _collections.abc.Callable) -> tuple[type, ...]:
    out_hint = _typing.get_type_hints(func)["return"]
    if isinstance(out_hint, _types.UnionType):
        out_types = _typing.get_args(out_hint)
    else:
        out_types = (out_hint,)
    return out_types

def _return_none(self: BaseCoordinate) -> None:
    return None



